			<p class="copy">
				Esteban should know. As he learned from his father, custom has it in the Philippines that when you visit someone older than you, you touch their forehead, as if to say, “Please pass your wisdom on to me.” Esteban was interested in bringing this thinking to Poland. And each time he travels there, he is an ambassador, figuratively touching the heads of the Polish people. “There is a continuum to life,” Esteban believes. “The wind-down is just as important as the&nbsp;wind-up.”<br><br>

				The wind-up of Immersion has been equally important. Two years on, Sustainable Aging and the rest of Immersion’s projects are quietly making headlines and earning far-ranging praise from those most integrated into the story lines: from relief workers in Brazil’s favelas to Eastern European heads of state. Janssen’s C-suite is equally pleased. Still, the constant pull of the status quo makes disruption on this scale, and Immersion specifically, tremendously difficult to keep&nbsp;going.<br><br>

				In part, this is due to the fact that the majority of public companies pledge allegiance to the short term. Call it an organization’s survival instinct. Or to paraphrase Harvard’s Christensen, “Even when an employee has a bold vision to take the company in a different direction, any company well-adapted to survival will reject the employee’s attempt to change direction.” Managers whose annual reviews hinge on quarterly gains see no advantage to incubating what appear to be pipe dream projects for their&nbsp;employees.<br><br>

				This is what makes management sponsorship such a crucial component to the disruption equation. Having highly motivated employees such as Daems and Esteban who were willing to engage in a breakthrough conversation was not enough for Immersion to become a reality. Their idea needed an advocate in Thomas. And that’s exactly what&nbsp;it&nbsp;got.<br><br>

				But it needed&nbsp;more.

				</p>
