				<p class="copy">

				An obstacle for Immersion’s early participants was that they weren’t used to starting a project from nothing: there were no data, no assignments, no timelines. If you were interested in an Immersion project, you had to decide for yourself what you would take on. In some cases, even the projects weren’t a given. Each had its own trajectory. Some started slowly and quietly built momentum while others skyrocketed then&nbsp;crashed.<br><br>

				This de-emphasis on conventional planning and productivity was the force behind Immersion, and one of the key reasons why it was so hard to get early executive buy-in from anyone other than Thomas. The more checklists an organization can adhere to, the more codified the processes, the greater the productivity and the higher the margins. Immersion had none of this. There were no benchmarks because no one had ever taken on these issues. There were no forecasts because no one knew exactly where they were&nbsp;going.<br><br>

				Most people wouldn’t be able to survive in this environment, much less&nbsp;thrive. <br><br>
					
				But the discovery-driven process of Immersion suited Debbie Nicholl just fine. One of the initial team leaders for TB Peru, Nicholl had been diving into unknown waters her entire life. Reared in rural Ontario by English parents, from an early age Nicholl was entranced by Robin Hood, heroic outlaw known for robbing from the rich and giving to the poor—simple, honest-to-goodness asset&nbsp;reallocation.<br><br>

				Nicholl’s inspiration guided her through her university studies, where she discovered life sciences, epidemiology, the study of disease, and genetics. She thought she would end up working for the International Red Cross or the Center for Disease Control, but an economics class her senior year provided a new vision for her future. Pursuing International Health Policy at the London School of Economics, her degree took her to Cambodia for the World Bank, and eventually to Janssen. Nicholl’s work has focused on improving public health worldwide by building sustainable access models through innovative&nbsp;partnerships.

