			<p class="copy">
				Ross Underwood is no stranger to global disruption. Living in the heart of Mexico in December 1994, he woke up early one morning to learn that the inventory of poultry vaccines he was selling for a major pharmaceutical company had, overnight, decreased in value by a half million dollars. The Mexican peso had crumbled. It was a crisis he’d relive three years later in Taiwan when the Thai baht fell. As he headed toward South Korea trying to outrace the collapse, he was arrested by government police for the earlier misdeeds of a long-gone&nbsp;colleague.<br><br>

				By 2002, Underwood was back in Taiwan. He and his family were quarantined for nearly five months during the SARS outbreak. A tall man with a solemn face, one might view Underwood and his career as a perfect example of being in the wrong place at the wrong time. But that’s not how he sees it. His experiences shaped him into the leader he is today at Janssen, allowing him to feel right at home in unknown territory. It’s what made him jump at the chance to be a part of Janssen’s latest grassroots breakthrough project:&nbsp;Immersion.<br><br>

				Immersion is a textbook case of disruptive innovation, as defined by Harvard Business School’s Clayton Christensen in his seminal work, The Innovator’s Dilemma. The concept for this kind of innovation is simple. One: identify specific problems, such as tuberculosis (TB) in Peru, Hepatitis C (HCV) in Romania, or sustainable aging in Poland. Two: assemble small, start-up-like teams to solve those problems. Three: get those teams out of the office and on the ground, working, playing and plunging into the cultural waters where the problems are, and where none of your competitors are playing. Four: change the healthcare game in developing markets, change the game globally.
				Like most ideas, the spark for Immersion was ignited while trying to solve an overlooked problem. Two Janssen employees working on different continents—European-based Annick Daems and her stateside colleague Enrique Esteban—had been entrusted to spearhead a diversity and inclusion initiative for the&nbsp;organization. <br><br>

				It was a perfect, if daunting, project for the&nbsp;two. <br><br>

				Daems, a Belgian native, had grown up with Janssen. Her mother worked for the organization for thirty years, and Janssen was where Daems landed her first job. Like two of her childhood heroes, the plucky Belgian comic characters Suske and Wiske, Annick loved to explore. Her aptitude for languages (French, Dutch, German, English and Spanish) along with her teenage realization that she was gay, had led Daems to a deep interest in cross-border communication.
				Esteban, meanwhile, had been raised in the Philippines by his Filipino father and first-generation German-American mother. Esteban’s grandparents moved to the United States before World War II, and in the 60s, his mother traveled back to Germany to study, where she met Esteban’s father who was there studying law. After nearly dying from asthma as a child, Esteban grew up with a fighter’s spirit, constantly willing to test the boundaries of life. A brief stint in international development and business school, and he found himself at&nbsp;Janssen.

