				As Daems and Esteban began their research on the diversity and inclusion initiative, a strange finding showed up for them. They noted that the majority of their peers advising Janssen on emerging markets had never stepped foot inside of the respective markets’ countries. In addition, the two realized that the organization was facing another problem common within large companies: employees working in silos. How could their diversity and inclusion project get off the ground if Janssen teams were operating within their own echo&nbsp;chambers? <br><br>

				Daems and Esteban had an&nbsp;idea.<br><br>

				Convinced that throwing together an eclectic mix of people and skill sets would generate unexpected ideas and disruptive outcomes, the two quietly approached Adrian Thomas. As head of Janssen’s Global Market Access and Public Health, Thomas, a physician by training, was under mandate to find scalable solutions to global health problems. Listening to Daems and Esteban speak, Thomas innately understood the potential in the one-to-one interactions the two were proposing. With his full endorsement and oversight, Immersion was&nbsp;born.<br><br>

				As one of the first projects to come out of Immersion, Esteban headed up solving Poland’s sustainable aging problem. On the front lines of a global aging population crisis, Poland is watching fertility rates fall as life expectancy continues to rise. “There aren’t enough young working people to support the financial burden of geriatric care,” Esteban notes. “So the elderly are being spit out of the Polish system. They’re being shunned. We have a lot to learn from these&nbsp;people.”  <br><br>
