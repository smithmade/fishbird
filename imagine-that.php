			<h2>
				HOW TO TRAIN YOUR BRAIN TO MASTER UNCHARTED TERRAIN
			</h2>
			<div class="published">originally published by <a href="http://www.fastcompany.com/3024534/bottom-line/how-to-train-your-brain-to-master-uncharted-terrain">Fast Company</a></div>

			<p class="copy">
				It took me three hours to figure out exactly what Jason Watt does for a living. He's a branding guy. He's a leadership coach. He's a restaurateur. Tomorrow, he might call himself something else entirely. He'll be the first to admit--even he couldn't&nbsp;tell&nbsp;you.
				<br><br>
				When we met last month, Watt and his business partners had just bought seven houses in Burlington,NJ, with plans to revitalize the town. "Today our mission is rebuilding great American cities," he&nbsp;told&nbsp;me.
				<br><br>
				"It is?" said his PR agent, sitting across the table from him. "That's a new one, even&nbsp;for&nbsp;me."
				<br><br>
				But that kind of uncertainty and surprise is exactly what Watt wants. "We actually don’t know what we are doing," he admits. "Most people don’t do things if they don’t know what they are doing. We are acknowledging that we are making it up and it's not a&nbsp;set&nbsp;process."
				<br><br>
				Before they opened their first restaurant, <a href="http://www.brickwalltavern.com">Brickwall</a>, in 2006 in Asbury Park, N.J., Watt and his partners had virtually no restaurant experience. They were running a branding agency and had started offering leadership coaching through a firm they called&nbsp;Fishbird.
				<br><br>
				But coaching companies--even with big-name clients like Virgin Mobile and Johnson &amp; Johnson--wasn't satisfying enough. They liked food, so restaurants seemed like a good idea. So far it's worked out. Watt and his partners have joined forces with an architecture firm and they call themselves <a href="http://www.smithmade.org">Smith</a>. Today, their restaurants in Asbury Park have helped to revitalize the city. In March, they are opening their seventh restaurant and recently bought up real estate in Burlington, N.J., where they plan to start building and opening more&nbsp;restaurants.
				<br><br>
				Breaking into unfamiliar territory wasn't easy for Watt and his partners, but he says these eight principles helped them along&nbsp;the&nbsp;way:
				</p>

<!-- side snippet -->

