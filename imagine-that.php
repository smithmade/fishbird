				<p class="copy">
				<strong>1. LET YOURSELF BE TERRIFIED.</strong>
				<br><br>
				When Watt and his partners decided to jump from branding and leadership training to buying up property and opening their own restaurants, he admits he was a bit terrified. It could all be a giant flop. But that terror is a feeling he's grown familiar with. "There needs to be a feeling of terror that you have to get over every day," he says. "That to me is a good sign and not a&nbsp;bad&nbsp;sign."
				<br><br>
				<strong>2. DON'T KNOW WHAT YOU'RE DOING? FIND SOMEONE WHO&nbsp;DOES.</strong>
				<br><br>
				Today Smith has more than 350 employees and is growing by the day. When Watt is hiring, he could care less what was on a person's resume. If your personality isn't a good match, all the experience in the world won't help you get hired. But beyond personality, he says: "You must surround yourself with people who are smarter than you. That is absolutely key." When the idea of starting a restaurant first came up, Watt and his partners turned to his brother, architect Jim Watt to design the space. He has since partnered with Smith in designing all its&nbsp;restaurants.
				<br><br>
				<strong>3. QUIT BEING A CRITIC.</strong>
				<br><br>
				If Watt and his partners had been too quick to judge their idea, they never would have opened their first restaurant. After all, none of them had ever built or opened a restaurant before, besides one partner who'd run her family's deli. "Being judgmental too quickly is the downfall to almost every brand," says Watt. "Judgment gets in the way of design." Early on, you're better off imagining that all your ideas are great ideas. Write them down. Let them get a little crazy. "You have to let people dream and say crazy shit before they decide what they want to do," says&nbsp;Watt.
				<br><br>
				<strong>4. SLOW DOWN. SUCCESS TAKES TIME.</strong>
				<br><br>
				It took two years before Smith opened their first restaurant. They had to figure out how to do it, for starters. "People want to execute ideas too quickly. They haven't incubated them sufficiently," says Watt. You don’t get a royal flush the first time you deal your cards. You have to pick some up and put some down, pick some more up and put&nbsp;them&nbsp;down."
				<br><br>
				<strong>5. MAKE CREATIVITY A DAILY PRACTICE.</strong>
				<br><br>
				If you want to break into a new field, it's important to make time for creativity every day. Often people associate creative thinking with just starting out and they go into execution mode after laying down the groundwork. But that's not enough. "You need to be creative every day," says Watt. It might take 600 attempts to find the right solution, but if you never let yourself get past your fifth attempt, you're 595 tries away from getting&nbsp;there.
				<br><br>
				<strong>6. QUIT DWELLING IN THE PAST.</strong>
				<br><br>
				Often, we let the past dictate what we are capable of doing in the future. Big mistake. The past can help you identify what you want to do moving forward, but it shouldn't be a marker of what you're capable of doing. "If something already exists and you look to create it again, you are starting inside of constraints already," says&nbsp;Watt.
				<br><br>
				<strong>7. BE PREPARED FOR YOUR VIEW TO CHANGE.</strong>
				<br><br>
				When you're driving, you don't stop paying attention to the road ahead. Your view is constantly changing and what lies in front of you determines what action you take. "In leadership, it's imperative to always be looking because your view will change year-to-year, week-to-week, day-to-day." Don't let yourself go&nbsp;on&nbsp;autopilot.
				<br><br>
				<strong>8. FIND PERFECTION IN THE IMPERFECT.</strong>
				<br><br>
				The Japanese aesthetic wabi-sabi is based on the notion of seeking perfection in the imperfect. It's a concept Smith lives by. "Build a business and let it have personality," says Watt. "It needs to be wabi-sabi. It needs to have character&nbsp;and&nbsp;warmth."
			</p>


