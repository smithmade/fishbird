				<p class="copy">

				Partnering with Nuyts to cultivate this belief that We Are Them was Jami Taylor, who felt that she’d been training her entire life to advocate for a more robust and appropriate view of public health on the world stage. Growing up in Washington, D.C. as part of a political family, one of Taylor’s first memories was of holding campaign signs and attending political fundraisers before school. It was during these formative years that she learned how to mobilize people in support of&nbsp;an&nbsp;idea. <br><br>

				Being invited into Immersion has been a thrill for Taylor; she’s loved facilitating conversations with government leaders from Peru to Romania to Poland. As Nuyts shares, “The best part of Immersion is solving problems in a holistic way: there is no doubt that this is the future of&nbsp;healthcare.”<br><br>

				Implementing disruptive ideas is an exacting endeavor, in part because it involves disruption of self. Individuals must dive into the depths of the unknown even as they wonder if there will be enough breathing room within the organization as they come up for air. There have been times when people involved in Immersion, after being in the field all day, must catch up on their  day job at night. For some, this has been a real career risk. It also requires learning to communicate in new ways. Some are naturals at cold calling and enlisting help, others initially communicated awkwardly and are only now learning to speak powerfully about their&nbsp;vision.<br><br>

				And yet, as Immersion participants approach public health issues from a broader strategic and emotional perspective than a typical product launch, they are building new alliances and discovering new stakeholders both inside and outside Janssen, from government officials to entrepreneurs and NGOs. “The speed and initiative that people are taking on, and the enthusiasm about something new, has been nothing short of inspiring,” shares Underwood, now overseeing the transformed TB Peru project, which under his leadership has refocused on mHealth, a digital adherence platform that will soon be piloted in&nbsp;Uganda. <br><br>

				Nicholl echoes this Underwood’s sentiment. “I’ve gotten to see individuals excel. As Immersion captured their imagination they’ve really come to life.” Esteban agrees. “Every time I go to Poland, there are tremendously satisfying moments: engagement and enrollment with Janssen colleagues, as well as external stakeholders, governments, and private corporations.” For many, Immersion is the best part of their work. And while the people behind Immersion may change as projects continue to grow, the disruptive spirit that has been created will carry Janssen, and healthcare as a whole,&nbsp;forward.					

