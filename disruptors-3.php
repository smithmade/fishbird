			<p class="copy">

				And as with all Immersion projects, these partnerships couldn’t exist without dismantling the existing Us v. Them mentality within Janssen and building up a We Are Them mindset. The literature on disruptive innovation tends to frame disruption as a battle: the heroic David against the oafish Goliath. But the challenge for large organizations is to find a way to tackle two distinct and often conflicting tasks—sustaining what exists and building something new. Acknowledging that David and Goliath are fighting the same battle was important for Immersion participants to establish. Silos had to be broken to partner with talent from one P&amp;L to work on a project whose success may accrue to another&nbsp;P&amp;L.<br><br>

				This inevitably led to management conflicts over resource allocation, and the need for Thomas to emerge as a key stakeholder in the success of Immersion. Thomas was unique among early adopters of Immersion in seeing how such a dramatic shift in work roles could support the long-term priorities of Janssen as&nbsp;a&nbsp;whole.<br><br>

				Cultivating this We Are Them mentality by working alongside colleagues and building alliances internally and externally has been pivotal for Immersion team leader Guy Nuyts’s success. In high school, Nuyts had a fascination with politics, an interest fueled by a teacher who taught him Hegel’s premise: you can’t have a master without a slave, a yin without a yang, a you without a me. After studying sociology, he pursued his doctorate, during which time he worked in a hospital and became interested in epidemiology, eventually leading him to Janssen, where he’s worked for the last sixteen&nbsp;years.<br><br>

				As Nuyts launched MDR-TB Europe and became intimately involved in Romania’s HCV project, he saw the opportunity to discard the Us v. Them paradigm. While the opinion influencers in Romania have been those who were prescribing the drugs, Nuyts discovered early on that the HCV Immersion goal of increasing treatment to 80,000 people by 2020 was going to require a much larger group of stakeholders, including the World Health Organization and the Romanian Ministry of Health. The focus needed to shift away from short-term profits derived from selling to lining up and working with the various stakeholders in order to raise awareness, increase screening, register patients and treat&nbsp;them. <br><br>			
		
				A small but pivotal win came earlier this year. Nuyts was part of a Janssen delegation, alongside competitors AmGen and Eli Lilly, to confer with the Romanian Minister of Health on methods for tackling issues of public health. And since Janssen was focused on long-term results for Romania, rather than on short-term pricing wins, they received the full attention of the minister and an invitation to follow&nbsp;up.

				</p>
