			<h2>
				The New Disruptors: How&nbsp;Fishbird Is Transforming&nbsp;Pharma
			</h2>
			<!--
			<div class="published">originally published by <a href="http://www.fastcompany.com/3024534/bottom-line/how-to-train-your-brain-to-master-uncharted-terrain">Fast Company</a></div>
